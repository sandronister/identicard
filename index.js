const cartoes = {
    VISA: /^4[0-9]{12}(?:[0-9]{3})/,
    MASTER: /(?:5[1-5][0-9]{2}|222[1-9]|22[3-9][0-9]|2[3-6][0-9]{2}|27[01][0-9]|2720)[0-9]{12}/,
    AMEX: /^3[47][0-9]{13}/,
    DINNERS: /^3(?:0[0-5]|[68][0-9])[0-9]{11}/,
    DISCOVERY: /^6(?:011|5[0-9]{2})[0-9]{12}/,
    JCB: /^(?:2131|1800|35\d{3})\d{11}/,
    ELO:/^((((636368)|(438935)|(504175)|(451416)|(636297))\d{0,10})|((5067)|(4576)|(4011))\d{0,12})/
};



exports.cardValidate = (card) => {

    for(let cartao in cartoes){
        if(card.number.match(cartoes[cartao])) {
            card.flag = cartao
        }
    }
}



